-- Phase 4B: announcement publishing metadata and compliance-gated verified results.
-- Apply only to an isolated database until separately approved for production.

BEGIN;

CREATE EXTENSION IF NOT EXISTS pgcrypto;

ALTER TABLE public.content_items
    ADD COLUMN IF NOT EXISTS announcement_type TEXT,
    ADD COLUMN IF NOT EXISTS announcement_priority TEXT,
    ADD COLUMN IF NOT EXISTS announcement_audience TEXT,
    ADD COLUMN IF NOT EXISTS featured BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN IF NOT EXISTS pinned BOOLEAN NOT NULL DEFAULT FALSE,
    ADD COLUMN IF NOT EXISTS cta_label TEXT,
    ADD COLUMN IF NOT EXISTS cta_url TEXT,
    ADD COLUMN IF NOT EXISTS expires_at TIMESTAMPTZ,
    ADD COLUMN IF NOT EXISTS updated_by BIGINT REFERENCES public.users(id) ON DELETE SET NULL;

ALTER TABLE public.content_items
    DROP CONSTRAINT IF EXISTS content_items_announcement_type_check,
    DROP CONSTRAINT IF EXISTS content_items_announcement_priority_check,
    DROP CONSTRAINT IF EXISTS content_items_announcement_audience_check,
    DROP CONSTRAINT IF EXISTS content_items_announcement_dates_check;

ALTER TABLE public.content_items
    ADD CONSTRAINT content_items_announcement_type_check CHECK (
        announcement_type IS NULL OR announcement_type IN
        ('PLATFORM_UPDATE','MARKET_NOTICE','SIGNAL_UPDATE','SERVICE_NOTICE','EDUCATION','EVENT','GENERAL')),
    ADD CONSTRAINT content_items_announcement_priority_check CHECK (
        announcement_priority IS NULL OR announcement_priority IN ('NORMAL','IMPORTANT','URGENT')),
    ADD CONSTRAINT content_items_announcement_audience_check CHECK (
        announcement_audience IS NULL OR announcement_audience IN ('PUBLIC','MEMBERS','ALL')),
    ADD CONSTRAINT content_items_announcement_dates_check CHECK (
        expires_at IS NULL OR scheduled_at IS NULL OR expires_at > scheduled_at);

CREATE INDEX IF NOT EXISTS content_items_announcement_admin_idx
    ON public.content_items (status, updated_at DESC) WHERE content_type='ANNOUNCEMENT';
CREATE INDEX IF NOT EXISTS content_items_announcement_public_idx
    ON public.content_items (pinned DESC, published_at DESC, id DESC)
    WHERE content_type='ANNOUNCEMENT' AND is_public=TRUE AND is_published=TRUE AND deleted_at IS NULL;

CREATE TABLE IF NOT EXISTS public.verified_results (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,
    related_signal_id BIGINT REFERENCES public.market_signals(id) ON DELETE SET NULL,
    symbol TEXT NOT NULL,
    direction TEXT NOT NULL CHECK (direction IN ('BUY','SELL')),
    timeframe TEXT,
    entry_price NUMERIC(18,6) NOT NULL CHECK (entry_price > 0),
    exit_price NUMERIC(18,6) NOT NULL CHECK (exit_price > 0),
    stop_loss NUMERIC(18,6),
    targets JSONB NOT NULL DEFAULT '[]'::JSONB,
    lifecycle_outcome TEXT NOT NULL,
    result_unit TEXT NOT NULL DEFAULT 'POINTS' CHECK (result_unit IN ('POINTS','PIPS')),
    result_value NUMERIC(18,6),
    result_points NUMERIC(18,6) NOT NULL,
    result_percentage NUMERIC(9,4),
    calculation_basis TEXT NOT NULL DEFAULT 'EXIT_MINUS_ENTRY',
    opened_at TIMESTAMPTZ NOT NULL,
    closed_at TIMESTAMPTZ NOT NULL,
    evidence_type TEXT,
    evidence_media_id BIGINT REFERENCES public.media_assets(id) ON DELETE SET NULL,
    evidence_notes TEXT,
    redaction_confirmed BOOLEAN NOT NULL DEFAULT FALSE,
    verification_status TEXT NOT NULL DEFAULT 'DRAFT'
        CHECK (verification_status IN ('DRAFT','PENDING_EVIDENCE','PENDING_REVIEW','VERIFIED','REJECTED','ARCHIVED','TRASHED')),
    rejection_reason TEXT,
    verified_by BIGINT REFERENCES public.users(id) ON DELETE SET NULL,
    verified_at TIMESTAMPTZ,
    compliance_status TEXT NOT NULL DEFAULT 'PENDING'
        CHECK (compliance_status IN ('PENDING','PASSED','FAILED')),
    compliance_notes TEXT,
    public_summary TEXT,
    featured BOOLEAN NOT NULL DEFAULT FALSE,
    publication_status TEXT NOT NULL DEFAULT 'DRAFT'
        CHECK (publication_status IN ('DRAFT','PUBLISHED','UNPUBLISHED','ARCHIVED','TRASHED')),
    published_at TIMESTAMPTZ,
    created_by BIGINT REFERENCES public.users(id) ON DELETE SET NULL,
    updated_by BIGINT REFERENCES public.users(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    deleted_at TIMESTAMPTZ,
    CONSTRAINT verified_results_dates_check CHECK (closed_at >= opened_at),
    CONSTRAINT verified_results_percentage_check CHECK (result_percentage IS NULL),
    CONSTRAINT verified_results_publish_gate CHECK (
        publication_status <> 'PUBLISHED' OR
        (verification_status='VERIFIED' AND compliance_status='PASSED' AND
         evidence_media_id IS NOT NULL AND redaction_confirmed=TRUE AND verified_at IS NOT NULL))
);

CREATE INDEX IF NOT EXISTS verified_results_admin_idx
    ON public.verified_results (verification_status, publication_status, updated_at DESC);
CREATE INDEX IF NOT EXISTS verified_results_public_idx
    ON public.verified_results (featured DESC, published_at DESC, id DESC)
    WHERE publication_status='PUBLISHED' AND verification_status='VERIFIED'
      AND compliance_status='PASSED' AND deleted_at IS NULL;
CREATE INDEX IF NOT EXISTS verified_results_signal_idx ON public.verified_results (related_signal_id);

ALTER TABLE public.verified_results ENABLE ROW LEVEL SECURITY;
REVOKE ALL ON TABLE public.verified_results FROM anon, authenticated;

COMMIT;
