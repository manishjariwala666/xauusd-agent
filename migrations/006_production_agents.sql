CREATE OR REPLACE FUNCTION public.enqueue_due_ai_agent_schedules()
RETURNS TABLE(job_id BIGINT)
LANGUAGE plpgsql
SECURITY INVOKER
AS $$
BEGIN
    RETURN QUERY
    WITH due AS (
        SELECT s.id, s.agent_id, s.payload, s.interval_minutes
        FROM public.ai_agent_schedules s
        JOIN public.ai_agents a ON a.id = s.agent_id
        WHERE s.is_enabled = TRUE
          AND a.is_enabled = TRUE
          AND s.next_run_at <= NOW()
        FOR UPDATE OF s SKIP LOCKED
    ), queued AS (
        INSERT INTO public.ai_agent_jobs (agent_id, payload)
        SELECT agent_id, payload FROM due
        RETURNING id, agent_id
    ), advanced AS (
        UPDATE public.ai_agent_schedules s
        SET last_enqueued_at = NOW(),
            next_run_at = NOW() + make_interval(
                mins => (SELECT d.interval_minutes FROM due d
                         WHERE d.id = s.id)
            ),
            updated_at = NOW()
        WHERE s.id IN (SELECT id FROM due)
        RETURNING s.agent_id
    )
    SELECT q.id FROM queued q;
END;
$$;

INSERT INTO public.ai_agent_schedules (
    agent_id, interval_minutes, next_run_at
)
SELECT id,
       CASE agent_key
           WHEN 'signal_agent' THEN 5
           WHEN 'announcement_agent' THEN 1
           WHEN 'seo_agent' THEN 1440
           ELSE 60
       END,
       NOW() + INTERVAL '5 minutes'
FROM public.ai_agents
WHERE agent_key IN ('signal_agent', 'announcement_agent', 'seo_agent')
ON CONFLICT (agent_id) DO NOTHING;

UPDATE public.ai_agents a
SET schedule_minutes = s.interval_minutes,
    next_scheduled_run_at = s.next_run_at
FROM public.ai_agent_schedules s
WHERE s.agent_id = a.id;

