def upload(client: TestClient, filename: str = "chart.png"):
    return client.post("/admin/media/upload", headers=headers(), files={"file": (filename, png_bytes(), "image/png")}, data={"alt_text": "Gold market chart", "caption": "Synthetic local fixture"})


def test_migration_apply_rollback_reapply_and_upload_metadata(client: TestClient, engine) -> None:
    first = upload(client); second = upload(client)
    assert first.status_code == second.status_code == 201
    assert first.json()["stored_filename"] != second.json()["stored_filename"]
    media_id = first.json()["id"]
    updated = client.patch(f"/admin/media/{media_id}", headers=headers(), json={"alt_text": "Updated accessible chart", "caption": "Updated caption"})
    assert updated.status_code == 200 and updated.json()["alt_text"] == "Updated accessible chart"
    listing = client.get("/admin/media?search=accessible", headers=headers()).json()
    assert listing["total"] == 1 and listing["items"][0]["thumbnail_url"].endswith(".webp")
    with engine.connect() as connection:
        events = set(connection.execute(text("SELECT event_type FROM admin_auth_audit_events")).scalars())
    assert {"MEDIA_UPLOADED", "MEDIA_METADATA_UPDATED"}.issubset(events)


@pytest.mark.parametrize(("filename", "mime", "data", "expected"), [
    ("fake.jpg", "image/jpeg", png_bytes(), 400),
    ("unsafe.png", "application/x-php", png_bytes(), 400),
    ("../escape.png", "image/png", png_bytes(), 400),
    ("large.png", "image/png", b"0" * (8 * 1024 * 1024 + 1), 413),
])
def test_upload_validation_rejects_unsafe_files(client: TestClient, filename: str, mime: str, data: bytes, expected: int) -> None:
    response = client.post("/admin/media/upload", headers=headers(), files={"file": (filename, data, mime)})
    assert response.status_code == expected
    assert "Traceback" not in response.text


