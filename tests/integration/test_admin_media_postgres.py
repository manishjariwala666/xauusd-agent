def test_featured_attach_remove_trash_restore_and_safe_delete(client: TestClient, engine) -> None:
    media = upload(client).json(); media_id = media["id"]
    with engine.begin() as connection:
        content_id = connection.execute(text("INSERT INTO content_items (title,is_published) VALUES ('Published local post',TRUE) RETURNING id")).scalar_one()
    attached = client.post(f"/admin/content/{content_id}/featured-image", headers=headers(), json={"media_id": media_id})
    assert attached.status_code == 200 and attached.json()["public_url"] == media["public_url"]
    assert client.post(f"/admin/media/{media_id}/trash", headers=headers()).status_code == 200
    blocked = client.delete(f"/admin/media/{media_id}?confirmed=true", headers=headers())
    assert blocked.status_code == 409
    assert client.post(f"/admin/media/{media_id}/restore", headers=headers()).status_code == 200
    assert client.post(f"/admin/media/{media_id}/trash", headers=headers()).status_code == 200
    assert client.delete(f"/admin/content/{content_id}/featured-image", headers=headers()).status_code == 200
    assert client.delete(f"/admin/media/{media_id}?confirmed=true", headers=headers()).status_code == 200
    with engine.connect() as connection:
        assert connection.execute(text("SELECT image_url FROM content_items WHERE id=:id"), {"id": content_id}).scalar_one_or_none() is None
        events = set(connection.execute(text("SELECT event_type FROM admin_auth_audit_events")).scalars())
    assert {"CONTENT_FEATURED_IMAGE_SET", "CONTENT_FEATURED_IMAGE_REMOVED", "MEDIA_DELETED"}.issubset(events)
